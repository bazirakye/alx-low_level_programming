#include "3-calc.h"

/**
 * op_add - add two numbers
 * @a: first number
 * @b: second number
 * Return: add
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtract two numbers
 * @a: first number
 * @b: second number
 * Return: sub
 */
int op_sub(int a, int b)
{
	return (a - b);
}


/**
 * op_mul - multiply two numbers
 *
 * @a: first number
 * @b: second number
 * Return: mul
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divides two numbers
 * @a: first number
 * @b: second number
 * Return: div
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - modulus two numbers
 * @a: first number
 * @b: second number
 * Return: mod
 */
int op_mod(int a, int b)
{
	return (a % b);
}
